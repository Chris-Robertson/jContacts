package com.jcontacts;

class Contact {
    private String name;
    private String phoneNumber;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getPhoneNumber() {
        return phoneNumber;
    }

    void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}
